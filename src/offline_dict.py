def get_offline_translation(text, target_language):
    """
    Look up text in dictionary.
    Returns: Translated Text or Original Text (if mismatch)
    """
    if target_language == "English":
        return text
    
    # Clean input: remove punctuation and whitespace
    clean_text = text.strip().rstrip('.!?').strip()
    
    # Direct Lookup
    if clean_text in OFFLINE_TRANSLATIONS:
        return OFFLINE_TRANSLATIONS[clean_text].get(target_language, clean_text)
    
    # Fallback: Try partial matching or case-insensitive
    for key, val in OFFLINE_TRANSLATIONS.items():
        if key.lower() == clean_text.lower():
             return val.get(target_language, clean_text)
             
    return text
